# Variable and Multiple Assignment

# Single variable
age = 24
print(age)

print("_____")

# String variable
sentence = "my name is rio"
print(sentence)

print("_____")

# Multiple Assignment (different values)
sarah, bob, mike = 16, 22, 19
print(sarah)
print(bob)
print(mike)

print("_____")

#Chained Assignment (same value)
sarah = bob = mike = 22
print(sarah)
print(bob)
print(mike)

print("_____")

# Mixed Assignment
name, age = "rio", 24
print(name)
print(age)
